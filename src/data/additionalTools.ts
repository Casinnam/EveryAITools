interface ToolSeed {
  id: string;
  name: string;
  websiteUrl: string;
  categoryId: string;
  pricingType: PricingType;
  startingPrice?: string;
  rating: number;
  beginnerFriendly: boolean;
  koreanSupport: boolean;
  mobileSupport: boolean;
  commercialUse: boolean;
  featured?: boolean;
  tags: string[];
  summary: string;
  bestFor: string;
  features: string[];
  pros: string[];
  cons: string[];
  useCases: string[];
}

const text = (en: string, ko = en) => ({ en, ko });

const makeTool = (seed: ToolSeed): Tool => ({
  id: seed.id,
  name: seed.name,
  slug: seed.id,
  description: text(seed.summary),
  longDescription: text(
    `${seed.name} is a practical AI tool best suited for ${seed.bestFor}. It is included here because it helps users make a clearer choice by comparing pricing, workflow fit, output quality, and ease of adoption.`,
  ),
  beginnerDescription: text(
    `Use ${seed.name} when you want help with ${seed.bestFor}. Start from a template, prompt, or simple workflow, then refine the result until it fits your project.`,
  ),
  websiteUrl: seed.websiteUrl,
  categoryId: seed.categoryId,
  pricingType: seed.pricingType,
  startingPrice: seed.startingPrice,
  rating: seed.rating,
  beginnerFriendly: seed.beginnerFriendly,
  koreanSupport: seed.koreanSupport,
  mobileSupport: seed.mobileSupport,
  commercialUse: seed.commercialUse,
  featured: seed.featured ?? false,
  tags: seed.tags,
  features: seed.features.map((feature) => text(feature)),
  pros: seed.pros.map((pro) => text(pro)),
  cons: seed.cons.map((con) => text(con)),
  useCases: seed.useCases.map((useCase) => text(useCase)),
});

